#include <stdio.h>

/**
 *
 *  * main - Entry here
 *
 *   * Description: prints all single digit numbers of base 10 starting from 0
 *
 *    * Return: 0
 *
 *     */

int main(void)

{

		int h;



			for (h = 0; h < 10; h++)

					{

								printf("%d", h);

									}

				printf("\n");

					return (0);

}


