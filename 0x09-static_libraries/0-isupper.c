/**
 * _isupper - function that checks for uppercase character.
 * @c: ASCII code of the character
 * Description: Prints uppercase character
 * Return: 1 (uppercase) or 0 (lowercase)
 */

int _isupper(int c)

{

	if (c >= 65 && c <= 90)

	{

	return (1);

	}

	else

	{

	return (0);

	}
}
